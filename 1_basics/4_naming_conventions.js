/**
 * Naming Conventions
 *
 * 변수 이름 지을 때
 * 1) 일반적으로 영어(라틴문자)를 사용하며 문자와 숫자를 모두 사용할 수 있다.
 * 2) 특기호는 언더스코어와 달러를 사용할 수 있다.
 * 3) 숫자로 이름을 시작 할 수 없다.
 *      1Name, 2Hello
 * 4) 키워드는 변수명으로 사용할 수 없다.
 *      var const = 'var';
 */

let codeFactory = '코드팩토리';
var $world = '세계';
const _juHyun = '현';

console.log(codeFactory);
console.log($world);
console.log(_juHyun);

